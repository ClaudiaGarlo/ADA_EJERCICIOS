//Ejemplo: Verificar si una persona es mayor de edad

//Paso 1: declaramos variable edad de la persona
let edad = 18

//Paso 2: usamos el operador ternario para determinar si es mayor de edad
let mensaje = edad >= 18 ? "Mayor de edad" : "Menor de edad";

//La condicion es: Edad mayo o igual a 18.
//Si es verdadero, el resultado será "Mayor de edad"
//Si es falso, el resultado será  "Menor de edad"

//Paso 3: mostramos por consola
console.log(mensaje);
